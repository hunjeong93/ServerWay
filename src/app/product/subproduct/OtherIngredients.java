package app.product.subproduct;

public class OtherIngredients {
    private String name;
    private int calorie;

    public OtherIngredients(String name, int calorie) {
        this.name = name;
        this.calorie = calorie;
    }

    public String getName() {
        return name;
    }

    public int getCalorie() {
        return calorie;
    }
}
