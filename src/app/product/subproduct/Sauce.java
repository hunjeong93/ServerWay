package app.product.subproduct;

public class Sauce {
    private String name;
    private int calorie;

    public String getName() {
        return name;
    }

    public int getCalorie() {
        return calorie;
    }
    public Sauce() {
    }

    public Sauce(String name, int calorie) {
        this.name = name;
        this.calorie = calorie;
    }


}
